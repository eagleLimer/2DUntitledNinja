package com.mygdx.game.gameData;

public class EntityData {
    private int id;
    private float xPos;
    private float yPos;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public float getxPos() {
        return xPos;
    }

    public void setxPos(float xPos) {
        this.xPos = xPos;
    }

    public float getyPos() {
        return yPos;
    }

    public void setyPos(float yPos) {
        this.yPos = yPos;
    }
}
